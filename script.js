let currentLang = "ja";

function render() {
  const t = content[currentLang];

  document.documentElement.lang = currentLang === 'zh' ? 'zh-CN' : currentLang === 'ja' ? 'ja' : 'en';
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined && typeof t[key] === 'string') el.textContent = t[key];
  });

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === currentLang);
  });

  const coverageList = document.getElementById('coverageList');
  coverageList.innerHTML = t.coverageItems.map(item => `
    <div class="coverage-item"><span class="coverage-dot"></span><span>${item}</span></div>
  `).join('');

  document.getElementById('servicesGrid').innerHTML = t.services.map(([title, text]) => `
    <article class="card"><h3>${title}</h3><p>${text}</p></article>
  `).join('');

  document.getElementById('strengthsGrid').innerHTML = t.strengths.map(([title, text]) => `
    <article class="card"><h3>${title}</h3><p>${text}</p></article>
  `).join('');

  document.getElementById('flowGrid').innerHTML = t.flow.map(([no, title, text]) => `
    <article class="card"><div class="section-label">${no}</div><h3>${title}</h3><p>${text}</p></article>
  `).join('');

  document.getElementById('companyRows').innerHTML = t.companyRows.map(([label, value]) => `
    <div class="company-row"><div class="company-label">${label}</div><div class="company-value">${value}</div></div>
  `).join('');

  document.getElementById('propertyGrid').innerHTML = properties.map(property => {
    const item = property[currentLang];
    return `
      <a class="property-card" href="${property.url}" target="_blank" rel="noreferrer">
        <div class="property-cover">
          <img src="${property.image}" alt="${item.title}">
          <div class="property-overlay"></div>
          <div class="property-badge">AIRBNB</div>
          <div class="property-bottom">
            <div class="property-name">${item.title}</div>
            <div class="property-meta">${item.meta}</div>
          </div>
        </div>
        <div class="property-body">
          <div class="property-text">${item.text}</div>
          <div class="property-link">${item.cta}</div>
        </div>
      </a>
    `;
  }).join('');
}

document.querySelectorAll('.lang-btn').forEach(btn => {
  btn.addEventListener('click', () => {
    currentLang = btn.dataset.lang;
    render();
  });
});

render();
